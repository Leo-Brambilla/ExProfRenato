//4. Dado os três lados de um triângulo determinar o perímetro do mesmo.

let ladoA = Number(prompt("Digite o valor do lado A "))
let ladoB = Number(prompt("Digite o valor do lado B "))
let ladoC = Number(prompt("Digite o valor do lado C "))
let perimetro = ladoA+ladoB+ladoC

alert(`O perímetro do seu triângulo é ${perimetro}`)